<script>
import HelperService from "@/services/helper.service";
import ServicePocketService from "@/services/ServicePocket.service";
import PaymentScheduleTypeService from "@/services/PaymentScheduleType.service";
export default {
  data() {
    return {
      SaveLoading: false,
      translatemodal: false,
      ServicePocket: { TranslatableColumns: [] },
      selectedrowid: 0,
      translitefields: [
        { key: "languagename", label: this.$t("languagename") },
        { key: "translatetext", label: this.$t("name") },
        { key: "actions", label: "" },
      ],
      translatelist: [],
      languagelist: [],

      translaterow: {
        tableid: 0,
        tablerowid: 0,
        languageid: 0,
        languagename: "",
        columnname: "",
        translatetext: "",
      },
      translatebyname: "",
      toastCount: 0,
      allSignupErrors: {},
      statelist: [],
      OrganizationList: [],
      PaymentScheduleTypeList:[],
    };
  },
  created() {
    var self = this;
    self.selectedrowid = self.$route.params.id;
    HelperService.GetLanguageList().then((res) => {
      self.languagelist = res.data;
    });
    HelperService.GetStateList().then((res) => {
      self.statelist = res.data;
    });
    ServicePocketService.Get(self.selectedrowid).then((res) => {
      self.ServicePocket = res.data;
    });
   PaymentScheduleTypeService.GetAll(self.lang).then((res) => {
      self.PaymentScheduleTypeList = res.data;
    });
  },
  methods: {
    check() {
      var self = this;

      if (
        self.ServicePocket.code === 0 ||
        self.ServicePocket.code === null ||
        self.ServicePocket.code === undefined ||
        self.ServicePocket.code === ""
      ) {
        this.makeToast(this.$t("codeNotCorrect"), this.$t("Error"), "danger");
        return false;
      }
      if (
        self.ServicePocket.shortname === 0 ||
        self.ServicePocket.shortname === null ||
        self.ServicePocket.shortname === undefined ||
        self.ServicePocket.shortname === ""
      ) {
        this.makeToast(
          this.$t("shortnameNotCorrect"),
          this.$t("Error"),
          "danger"
        );
        return false;
      }
      if (
        self.ServicePocket.fullname === 0 ||
        self.ServicePocket.fullname === null ||
        self.ServicePocket.fullname === undefined ||
        self.ServicePocket.fullname === ""
      ) {
        this.makeToast(
          this.$t("fullnameNotCorrect"),
          this.$t("Error"),
          "danger"
        );
        return false;
      }
      if (
        self.ServicePocket.visitcount === 0 ||
        self.ServicePocket.visitcount === null ||
        self.ServicePocket.visitcount === undefined ||
        self.ServicePocket.visitcount === ""
      ) {
        this.makeToast(
          this.$t("visitcountNotCorrect"),
          this.$t("Error"),
          "danger"
        );
        return false;
      }
    
      if (
        self.ServicePocket.sessiondurationid === 0 ||
        self.ServicePocket.sessiondurationid === null ||
        self.ServicePocket.sessiondurationid === undefined ||
        self.ServicePocket.sessiondurationid === ""
      ) {
        this.makeToast(
          this.$t("sessiondurationNotCorrect"),
          this.$t("Error"),
          "danger"  
        );
        return false;
      }
      if (
        self.ServicePocket.paymentscheduletypeid === 0 ||
        self.ServicePocket.paymentscheduletypeid === null ||
        self.ServicePocket.paymentscheduletypeid === undefined ||
        self.ServicePocket.paymentscheduletypeid === ""
      ) {
        this.makeToast(
          this.$t("paymentscheduletypeNotCorrect"),
          this.$t("Error"),
          "danger"
        );
        return false;
      }
  
      if (
        self.ServicePocket.detail === 0 ||
        self.ServicePocket.detail === null ||
        self.ServicePocket.detail === undefined ||
        self.ServicePocket.detail === ""
      ) {
        this.makeToast(
          this.$t("detailNotCorrect"),
          this.$t("Error"),
          "danger"
        );
        return false;
      }
   
      if (
        self.ServicePocket.stateid === 0 ||
        self.ServicePocket.stateid === null ||
        self.ServicePocket.stateid === undefined ||
        self.ServicePocket.stateid === ""
      ) {
        this.makeToast(
          this.$t("stateNotCorrect"),
          this.$t("Error"),
          "danger"
        );
        return false;
      }
      return true;
    },
    SaveData() {
      if (!this.check()) {
        return false;
        5;
      }
      var self = this;
      ServicePocketService.Update(self.ServicePocket)
        .then((res) => {
          self.makeToast(
            self.$t("SuccessMessage"),
            self.$t("message"),
            "success"
          );
          self.$router.push({ name: "ServicePocket" });
        })
        .catch((error) => {
          self.makeToast(
            error.response.data.error,
            self.$t("message"),
            "danger"
          );
        });
    },
    translate(selectcolumnname) {
      var self = this;
      self.translatebyname = selectcolumnname;
      self.translatelist = self.ServicePocket.Translates.filter(
        (item) => item.columnname == selectcolumnname
      );
      self.translatemodal = true;
    },
    addtranslite() {
      var self = this;
      if (
        self.translaterow.languageid === 0 ||
        self.translaterow.languageid === undefined ||
        self.translaterow.languageid === null
      ) {
        this.makeToast(
          this.$t("languageidNotSelect"),
          this.$t("Error"),
          "danger"
        );
      } else if (
        self.translaterow.translatetext === "" ||
        self.translaterow.translatetext === undefined ||
        self.translaterow.translatetext === null
      ) {
        this.makeToast(
          this.$t("translatetextNotSelect"),
          this.$t("Error"),
          "danger"
        );
      } else {
        var dublikate = false;
        self.translatelist.forEach((item) => {
          if (item.languageid === self.translaterow.languageid) {
            this.makeToast(this.$t("dublikate"), this.$t("Error"), "danger");
            dublikate = true;
          }
        });
        if (!dublikate) {
          self.translaterow.languagename = self.languagelist.filter(function(
            item
          ) {
            if (item.id === self.translaterow.languageid) return item;
          })[0].name;
          self.translaterow.columnname = self.translatebyname;
          self.translatelist.push(self.translaterow);
          self.translaterow = {
            tableid: 0,
            tablerowid: 0,
            languageid: 0,
            languagename: "",
            columnname: "",
            translatetext: "",
          };
        }
      }
    },
    SaveTranslate() {
      var self = this;
      self.ServicePocket.Translates = self.ServicePocket.Translates.filter(
        (x) => x.columnname !== self.translatebyname
      );
      self.translatelist.forEach((item) => {
        self.ServicePocket.Translates.push(item);
      });
      self.translatemodal = false;
    },
    makeToast(message, title, type) {
      var self = this;
      self.toastCount++;
      self.$bvToast.toast(message, {
        title: title,
        autoHideDelay: 2000,
        variant: type,
        solid: true,
      });
    },
    setAllErrors(errors) {
      this.allSignupErrors = errors;
    },
  },
};
</script>

